def leo(oscar)
  if oscar == 88
    return "Leo finally won the oscar! Leo is happy"
  end
  if oscar == 86
    return "Not even for Wolf of wallstreet?!"
  end
  if oscar < 88 && oscar != 86
    return "When will you give Leo an Oscar?"
  end
